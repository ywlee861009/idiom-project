package com.kero.idiom.notification

/**
 * 어르신들을 위한 따뜻하고 정중한 리마인더 문구 모음.
 */
enum class ReminderMessage(val title: String, val content: String) {
    WISDOM("오늘의 지혜가 기다리고 있습니다 📜", "사자성어 공부를 잊으신 건 아니죠? 서당의 문이 열려있습니다."),
    SCHOLAR("선비님, 글공부 하실 시간입니다 ✒️", "오늘의 성어 한 마디가 마음의 양식이 됩니다. 함께 시작해볼까요?"),
    LIBRARY("마음의 서고를 채워보세요 📖", "잊고 계셨던 사자성어들이 당신을 기다리고 있습니다."),
    RELAX("잠시 쉬어가는 지혜의 시간 🍵", "사자성어 퀴즈로 머리도 식히고 마음의 여유도 가져보세요."),
    TEACHER("훈장님의 말씀이 도착했습니다 👴", "오늘 하루를 지탱해 줄 귀중한 한 마디를 확인해보세요."),
    BRUSH("붓 끝에서 피어나는 지혜 🖌️", "다시 한 번 사자성어의 깊은 뜻을 새겨보는 건 어떨까요?"),
    BETTER("어제보다 더 나은 오늘 ☀️", "꾸준한 학습이 진정한 학문의 길입니다. 서당에서 뵙겠습니다."),
    LANTERN("서당의 등불이 켜졌습니다 🕯️", "당신의 열정을 기다리는 성어들이 이곳에 모여 있습니다."),
    TREASURE("나만의 보물창고, 서고 💎", "새로운 성어 카드를 수집하러 지금 바로 가보실까요?"),
    SENTENCE("오늘의 귀한 문장 한 마디 📝", "세상을 보는 눈이 달라지는 사자성어 공부, 지금 시작하세요.");

    companion object {
        /**
         * 무작위로 알림 메시지를 선택합니다.
         */
        fun getRandom(): ReminderMessage = entries.random()
    }
}
